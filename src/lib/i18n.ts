// Helper to get active translations for Server Components
export async function getTranslations() {
  const locale = await getLocale();
  return (locale === "ar" ? ar : en) as unknown as TranslationDict;
}

export async function getLocale() {
  try {
    const { cookies } = await import("next/headers");
    const cookieStore = await cookies();
    const val = cookieStore.get("locale")?.value;
    return (val === "ar" ? "ar" : "en") as "en" | "ar";
  } catch {
    return "en";
  }
}

// ✅ دالة للـ Pages Router
export function getLocaleFromRequest(req: any) {
  const val = req?.cookies?.locale;
  return (val === "ar" ? "ar" : "en") as "en" | "ar";
}

// ✅ دالة عامة للترجمات
export function getDictionary(locale: "en" | "ar") {
  return locale === "ar" ? ar : en;
}
type DeepStringify<T> = T extends string
  ? string
  : { [K in keyof T]: DeepStringify<T[K]> };

export type TranslationDict = DeepStringify<typeof ar>;
